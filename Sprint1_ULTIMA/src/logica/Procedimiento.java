package logica;

/**
 * @author María
 *
 */
public class Procedimiento {

	
	String numero;
	String nombre;
	
	
	
	/**
	 * Constructor para la clase Procedimiento
	 * @param numero
	 * @param nombre
	 */
	public Procedimiento(String numero, String nombre) {
		this.numero = numero;
		this.nombre = nombre;
	}



	/**
	 * @return the numero
	 */
	public String getNumero() {
		return numero;
	}



	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	
	
	
	
}
